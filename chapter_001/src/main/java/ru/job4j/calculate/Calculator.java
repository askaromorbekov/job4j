package ru.job4j.calculate;

public class Calculator {
    /**
     * Метод сложения
     * @param first - second
     * @return result
     */
    public double add(double first, double second) {
        return first + second;
    }
    /**
     * Метод вычитания
     * @param first - second
     * @return result
     */
    public double subtract(double first, double second) {
        return first - second;
    }
    /**
     * Метод деления
     * @param first - second
     * @return result
     */
    public double div(double first, double second) {
        return first / second;
    }
    /**
     * Метод умножения
     * @param first - second
     * @return result
     */
    public double multiple(double first, double second) {
        return first * second;
    }
}
